
class Vehicle {
  final String id;
  final String name;
  final String brand;
  final String model;
  final int year;
  final String imageUrl;
  final String description;
  final double price;

  Vehicle({
    required this.id,
    required this.name,
    required this.brand,
    required this.model,
    required this.year,
    required this.imageUrl,
    required this.description,
    required this.price,
  });
}